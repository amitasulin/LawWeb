export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const area = practiceAreas[params.slug];
  
  if (!area) {
    return {
      title: "לא נמצא | משרד עורכי דין",
    };
  }

  return {
    title: `${area.title} | עומר אסולין משרד עורכי דין`,
    description: area.description,
  };
}

